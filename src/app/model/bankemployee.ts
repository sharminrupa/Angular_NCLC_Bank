export class Employee{
name: string;
mobile: string;
email: string;
date: string;
post: string;
address: string;
password: string;

constructor(name, mobile, email, date, post, address, password){
    this.name = name;
    this.mobile = mobile;
    this.email = email;
    this.date = date;
    this.post = post;
    this.address = address;
    this.password = password;
}

}
